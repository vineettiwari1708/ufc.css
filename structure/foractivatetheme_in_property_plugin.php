function buildexo_property_filter_shortcode() {
    $locations = get_terms(['taxonomy' => 'property_location', 'hide_empty' => false]);
    $types = get_terms(['taxonomy' => 'property_type', 'hide_empty' => false]);
    $statuses = get_terms(['taxonomy' => 'property_status', 'hide_empty' => false]);

    ob_start();
    ?>
    <form method="GET" action="<?php echo esc_url(home_url('/properties/')); ?>">
        <select name="location">
            <option value="">Select Location</option>
            <?php foreach ($locations as $location): ?>
                <option value="<?php echo esc_attr($location->slug); ?>"><?php echo esc_html($location->name); ?></option>
            <?php endforeach; ?>
        </select>

        <select name="type">
            <option value="">Select Type</option>
            <?php foreach ($types as $type): ?>
                <option value="<?php echo esc_attr($type->slug); ?>"><?php echo esc_html($type->name); ?></option>
            <?php endforeach; ?>
        </select>

        <select name="status">
            <option value="">Select Status</option>
            <?php foreach ($statuses as $status): ?>
                <option value="<?php echo esc_attr($status->slug); ?>"><?php echo esc_html($status->name); ?></option>
            <?php endforeach; ?>
        </select>

        <input type="submit" value="Filter" />
    </form>
    <?php
    return ob_get_clean();
}
